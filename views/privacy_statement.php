<p>Ingangsdatum: [4/12/2024]
[Focus6] wij hechte grote waarde aan uw privacy en zet zich in om uw persoonsgegevens te beschermen. Dit privacybeleid legt uit hoe wij gegevens verzamelen, gebruiken, opslaan en verwijderen wanneer deze niet langer nodig zijn.

1. Welke gegevens verzamelen wij?
Wij kunnen de volgende soorten persoonsgegevens verzamelen wanneer u onze website bezoekt of onze diensten gebruikt:

Contactgegevens: zoals uw naam, e-mailadres en het bericht dat u invoert.
Formuliergegevens: informatie die u invult via onze contact- of registratieformulieren.
Technische gegevens: zoals IP-adressen, browsergegevens, en cookies (voor analytische doeleinden).
2. Hoe gebruiken wij uw gegevens?

Wij gebruiken uw gegevens voor de volgende doeleinden:
Om contact met u op te nemen naar aanleiding van uw vragen of verzoeken.
Om onze diensten aan u te leveren of te verbeteren.
Om wettelijke verplichtingen na te komen (zoals het bewaren van bepaalde gegevens voor administratieve doeleinden).
Voor analytische doeleinden om onze website te verbeteren.

3. Hoe bewaren wij uw gegevens?
Uw gegevens worden veilig opgeslagen in een beveiligde omgeving, beschermd door fysieke, technische en organisatorische maatregelen.
Wij bewaren uw gegevens alleen zolang als nodig is om de doeleinden te vervullen waarvoor ze zijn verzameld, tenzij langere opslag vereist is door wet- of regelgeving.

4. Wanneer worden uw gegevens verwijderd?
Persoonsgegevens worden verwijderd wanneer:

Ze niet langer nodig zijn voor het oorspronkelijke doel waarvoor ze zijn verzameld.
U ons verzoekt uw gegevens te verwijderen (indien wettelijk toegestaan).
Wij wettelijk verplicht zijn om gegevens te verwijderen.
Hoe verwijderen wij gegevens?
Gegevens worden op een veilige manier verwijderd of geanonimiseerd zodat deze niet langer herleidbaar zijn naar een individu.

5. Delen wij uw gegevens?
Wij delen uw gegevens alleen in de volgende gevallen:

Met derde partijen die ons helpen bij het leveren van onze diensten (zoals hostingproviders), mits zij gebonden zijn aan een geheimhoudingsplicht.
Wanneer dit wettelijk vereist is, bijvoorbeeld bij een juridische procedure of een gerechtelijk bevel.

6. Uw rechten
U heeft de volgende rechten met betrekking tot uw persoonsgegevens:

Inzage: U mag weten welke gegevens wij van u hebben opgeslagen.
Correctie: U kunt onjuiste of onvolledige gegevens laten corrigeren.
Verwijdering: U kunt verzoeken dat wij uw gegevens verwijderen als deze niet langer nodig zijn.
Bezwaar: U kunt bezwaar maken tegen het gebruik van uw gegevens voor specifieke doeleinden.
Neem contact met ons op via [contactgegevens] om gebruik te maken van uw rechten.

7. Cookies en tracking
Onze website maakt gebruik van cookies om:

De functionaliteit van de website te verbeteren.
Anonieme statistieken te verzamelen over het gebruik van onze website.
U kunt cookies beheren via uw browserinstellingen. Raadpleeg ons [cookiebeleid] voor meer informatie.



